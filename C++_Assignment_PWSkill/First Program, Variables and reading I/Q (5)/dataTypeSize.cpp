//Size of basic data types

#include <iostream>
using namespace std;

 int main(){

    cout<<"The size of integer data type is "<<sizeof(int)<<"bytes"<<endl;
    cout<<"The size of character data type is "<<sizeof(char)<<"bytes"<<endl;
    cout<<"The size of boolean data type is "<<sizeof(bool)<<"bytes"<<endl;
    cout<<"The size of floating point data type is "<<sizeof(float)<<"bytes"<<endl;
    cout<<"The size of void data type is "<<sizeof(void)<<"bytes"<<endl;

    return 0;

 }